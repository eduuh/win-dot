# PSMUX - tmux-compatible terminal multiplexer for Windows
Write-Host "Installing PSMUX (modern psmux 3.x with .tmux.conf support)..."
try {
    # If an old standalone install exists from earlier versions, clean it up.
    $legacyInstallDir = "$env:LOCALAPPDATA\psmux"
    if (Test-Path "$legacyInstallDir\psmux.exe") {
        $legacyVersion = (& "$legacyInstallDir\psmux.exe" version 2>&1 | Out-String)
        if ($legacyVersion -match 'psmux 0\.') {
            Write-Host "Removing legacy psmux 0.x install..." -ForegroundColor Yellow
            Get-Process -Name psmux, pmux, tmux -ErrorAction SilentlyContinue |
                ForEach-Object { Stop-Process -Id $_.Id -Force -ErrorAction SilentlyContinue }
            Remove-Item -Recurse -Force $legacyInstallDir -ErrorAction SilentlyContinue

            $userPath = [Environment]::GetEnvironmentVariable('Path', 'User')
            $cleaned = ($userPath -split ';' | Where-Object { $_ -and ($_ -notlike "*\psmux*") }) -join ';'
            [Environment]::SetEnvironmentVariable('Path', $cleaned, 'User')
            $env:Path = ($env:Path -split ';' | Where-Object { $_ -and ($_ -notlike "*\psmux*") }) -join ';'
        }
    }

    winget install --id marlocarlo.psmux --source winget `
        --accept-source-agreements --accept-package-agreements --silent
    if ($LASTEXITCODE -eq 0) {
        Write-Host "Installed PSMUX" -ForegroundColor Green
    } else {
        Write-Host "Warning: psmux install exited with code $LASTEXITCODE" -ForegroundColor Yellow
    }
}
catch {
    Write-Host "Warning: Failed to install PSMUX: $_" -ForegroundColor Yellow
}

