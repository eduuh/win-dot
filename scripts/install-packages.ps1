# Windows Package Installation Script

$ErrorActionPreference = "Stop"
$ProgressPreference = "SilentlyContinue"

function Write-Status($msg, $color = "Cyan") {
    Write-Host "==> $msg" -ForegroundColor $color
}

# Idempotently install a stub at the given path that sources the canonical
# ~/.config/powershell/profile.ps1. Safe to re-run; preserves anything the
# user added before the stub by appending the stub block if it isn't there.
function Install-ProfileStub {
    param(
        [Parameter(Mandatory)][string]$Path,
        [string]$Label = $Path
    )

    $marker  = "# >>> win-dot profile stub >>>"
    $endTag  = "# <<< win-dot profile stub <<<"
    $stub    = @"
$marker
# Auto-generated by scripts/install-packages.ps1. Sources the canonical
# pwsh profile so we don't have to track per-host `$PROFILE files
# (whose paths depend on OneDrive KFM, tenant name, and pwsh version).
`$localProfile = Join-Path `$HOME '.config\powershell\profile.ps1'
if (Test-Path `$localProfile) { . `$localProfile }
$endTag
"@

    $dir = Split-Path -Parent $Path
    if (-not (Test-Path $dir)) {
        New-Item -ItemType Directory -Path $dir -Force | Out-Null
    }

    if (Test-Path $Path) {
        $existing = Get-Content $Path -Raw
        # Detect either our markered stub OR a previously hand-written stub that
        # already sources the canonical profile, so we don't append duplicates.
        $hasMarker = $existing -like "*$marker*"
        $hasManualStub = $existing -match '\.config[\\/]powershell[\\/]profile\.ps1'
        if ($hasMarker -or $hasManualStub) {
            Write-Status "Profile stub already present at $Label" "Green"
            return
        }
        # Preserve pre-existing content; append the stub with a blank-line break.
        $separator = if ($existing.EndsWith("`n")) { "`n" } else { "`n`n" }
        Add-Content -Path $Path -Value "$separator$stub" -Encoding UTF8
        Write-Status "Appended profile stub to $Label" "Green"
    } else {
        Set-Content -Path $Path -Value $stub -Encoding UTF8
        Write-Status "Wrote profile stub at $Label" "Green"
    }
}

function Install-ProfileStubs {
    Write-Status "Wiring PowerShell profile stubs to ~/.config/powershell/profile.ps1..."

    # pwsh 7 — uses $PROFILE in the running pwsh.
    if (Get-Command pwsh -ErrorAction SilentlyContinue) {
        $pwsh7Profile = & pwsh -NoLogo -NoProfile -Command 'Write-Output $PROFILE.CurrentUserCurrentHost' 2>$null
        if ($pwsh7Profile) {
            Install-ProfileStub -Path $pwsh7Profile.Trim() -Label "pwsh 7 ($pwsh7Profile)"
        }
    } else {
        Write-Status "pwsh not found, skipping pwsh 7 stub" "Yellow"
    }

    # Windows PowerShell 5.1 — has its own $PROFILE under WindowsPowerShell\.
    if (Get-Command powershell -ErrorAction SilentlyContinue) {
        $ps51Profile = & powershell -NoLogo -NoProfile -Command 'Write-Output $PROFILE.CurrentUserCurrentHost' 2>$null
        if ($ps51Profile) {
            Install-ProfileStub -Path $ps51Profile.Trim() -Label "powershell 5.1 ($ps51Profile)"
        }
    } else {
        Write-Status "Windows PowerShell not found, skipping 5.1 stub" "Yellow"
    }
}

function Install-ScoopPackage($package) {
    try {
        $pkgName = $package -replace '^[^/]+/', ''
        if (scoop list $pkgName 2>$null) {
            Write-Status "$pkgName already installed" "Green"
            return $true
        }

        Write-Status "Installing $package..."
        scoop install $package *>&1 | Out-Null
        if ($LASTEXITCODE -eq 0) {
            Write-Status "Installed $package" "Green"
            return $true
        } else {
            Write-Status "Failed to install $package (exit code: $LASTEXITCODE)" "Yellow"
            return $false
        }
    }
    catch {
        Write-Status "Error installing $package`: $_" "Yellow"
        return $false
    }
}

function Install-WingetPackage($package) {
    try {
        Write-Status "Checking $package..."
        $installed = winget list --id $package --exact 2>$null
        if ($installed -match $package) {
            Write-Status "$package already installed" "Green"
            return $true
        }

        Write-Status "Installing $package..."
        winget install --id $package --source winget --accept-source-agreements --accept-package-agreements --silent
        if ($LASTEXITCODE -eq 0) {
            Write-Status "Installed $package" "Green"
            return $true
        } else {
            Write-Status "Failed to install $package (exit code: $LASTEXITCODE)" "Yellow"
            return $false
        }
    }
    catch {
        Write-Status "Error installing $package`: $_" "Yellow"
        return $false
    }
}

# Check if Scoop is installed, install if needed
if (-not (Get-Command scoop -ErrorAction SilentlyContinue)) {
    Write-Status "Installing Scoop package manager..."
    try {
        Set-ExecutionPolicy RemoteSigned -Scope CurrentUser -Force
        Invoke-WebRequest -UseBasicParsing -Uri get.scoop.sh | Invoke-Expression

        # Add Scoop to the current session path
        $env:PATH = "$env:USERPROFILE\scoop\shims;$env:PATH"

        # Verify Scoop is now available
        if (-not (Get-Command scoop -ErrorAction SilentlyContinue)) {
            throw "Scoop installation failed - command not found after install"
        }
        Write-Status "Scoop installed successfully" "Green"
    }
    catch {
        Write-Host "Failed to install Scoop: $_" -ForegroundColor Red
        exit 1
    }
} else {
    Write-Status "Scoop already installed" "Green"
}

