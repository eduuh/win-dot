# Microsoft.PowerShell.Preview's installer ships a buggy PATH entry
# (`...\7-preview\preview` — a non-existent subdir).  Add the correct dir
# to the User PATH so `pwsh` is resolvable without a logoff.
$pwshDir = "C:\Program Files\PowerShell\7-preview"
if (Test-Path (Join-Path $pwshDir 'pwsh.exe')) {
    $userPath = [Environment]::GetEnvironmentVariable('Path', 'User')
    if (($userPath -split ';') -notcontains $pwshDir) {
        $newPath = if ($userPath) { "$userPath;$pwshDir" } else { $pwshDir }
        [Environment]::SetEnvironmentVariable('Path', $newPath, 'User')
        $env:Path = "$env:Path;$pwshDir"
        Write-Host "Added '$pwshDir' to User PATH" -ForegroundColor Green
    }
}

